import Foundation
import UIKit

extension UIColor {
    
    static func jazzColor() -> UIColor {
        return UIColor(red: 0.17, green: 0.17, blue: 0.37, alpha: 1.00)
    }
    
    static func purChemColor() -> UIColor {
        return UIColor(red: 0.40, green: 0.40, blue: 0.51, alpha: 1.00)
    }
    
    static func raisinBlackColor() -> UIColor {
        return UIColor(red: 0.56, green: 0.58, blue: 0.65, alpha: 1.00)
    }
}
